// Greet a user by name
function greet(name) {
  return "Hello, " + name + "!";
}

// Function 1
function intro(className) {
  return "This is " + className + "class.";
}

// Function 2
function plusOne(x) {
  return x + 1;
}

// Function 3
function convertDegree(x) {
  return x + "°F is "+ (x-32)*5/9 + "°C.";
}

// Function 4
function square(x) {
  return x * x;
}

// Function 5
function signal(color) {
  if (color === "green") {
    return "Go";
  } else {
    return "Stop";
  };
}
